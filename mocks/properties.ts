export function filterProperties(
  properties: Property[],
  transactionType?: string,
  location?: string,
  lifestyles?: LifestyleType[]
): Property[] {
  return properties.filter(property => {
    const matchesTransaction = !transactionType || property.listingType === transactionType;
    const matchesLocation = !location || property.location.city === location;
    const matchesLifestyle = !lifestyles || lifestyles.length === 0 || 
      lifestyles.some(lifestyle => property.lifestyle.includes(lifestyle));
    
    return matchesTransaction && matchesLocation && matchesLifestyle;
  });
}
